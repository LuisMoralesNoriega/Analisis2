    //metodo para iniciar tablero
    public void iniciarta(){
        int filast = this.labe.filas + 1;
        int colt = this.labe.columnas + 1;
        this.tablero = new Boton[filast][colt];
        int anchot = jPanel1.getWidth();
        int altot = jPanel1.getHeight();
        int anchoc = anchot / colt; 
        int altoc = altot / filast;        
        for(Casilla item : this.labe.lab){
            //System.out.println("Cas: " + item.tipo + " Fila: " + item.fila + " Colu: " + item.columna);
            this.tablero[item.fila][item.columna] = new Boton(anchoc * item.columna, altoc * item.fila, anchoc, altoc);
            //this.tablero[item.fila][item.columna].CambiarNombre(item.fila, item.columna);
            if(item.tipo == 1){
                 this.tablero[item.fila][item.columna].CambiarColor(1);
            }else if(item.tipo == 2){
                this.tablero[item.fila][item.columna].CambiarColor(2);
            }else if(item.tipo == 3){
                this.tablero[item.fila][item.columna].CambiarColor(3);
            }
            jPanel1.add(this.tablero[item.fila][item.columna]);            
        }        
        this.paint(this.getGraphics());         
    }
    
