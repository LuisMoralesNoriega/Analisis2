    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton para ingresar tablero:
        this.labe.lab.clear();
        String labe = JOptionPane.showInputDialog("Ingrese cadena de laberinto");
        String[] filas = labe.split(",");
        int fil = 0;
        int co =0;
        for (String item : filas) {
            String f = item.replace(" ", "");
            int col = 0;
            for(int i = 0; i < f.length(); i++ ){
               // System.out.println( "Car: " + f.charAt(i) + " Fil: " + fil + " Col:" + col );
                Casilla nueva = new Casilla(Integer.parseInt("" + f.charAt(i)), fil, col);
                this.labe.lab.add(nueva);
                co = col;
                col++;
            }            
            fil++;
        }
        this.labe.filas = fil - 1;
        this.labe.columnas = co;
        for (Casilla item : this.labe.lab) {
            System.out.println("Cas: " + item.tipo + " Fila: " + item.fila + " Colu: " + item.columna);
        }
        
        this.iniciarta();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(this.labe.lab != null){              
            Algoritmo al = new Algoritmo(this.labe.lab,this.labe.filas,this.labe.columnas);
            //encontrar casilla de inicio
            al.EncontrarInicio();
            //encontrar caminos
            al.EncontrarCaminos();
            //imprimir soluciones
            this.jTextArea1.setText(al.Soluciones);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

